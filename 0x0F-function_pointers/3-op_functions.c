/**
 * op_add - add two numbers
 *
 * @a: first number
 * @b: second number
 * Return: the sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract two numbers
 *
 * @a: first number
 * @b: second number
 * Return: the result
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two numbers
 *
 * @a: first number
 * @b: second number
 * Return: the result
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two numbers
 *
 * @a: first number
 * @b: second number
 * Return: the result
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - find the module of two numbers
 *
 * @a: first number
 * @b: second number
 * Return: a % b
*/
int op_mod(int a, int b)
{
	return (a % b);
}
